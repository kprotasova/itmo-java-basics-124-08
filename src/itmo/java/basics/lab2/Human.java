package itmo.java.basics.lab2;

public class Human {
    private byte age;
    private String name;
    private String sex;
    private int height;
    private int weight;

    public Human() {};

    public Human (byte age, String name, String sex, int height, int weight) {
        this.age = age;
        this.name = name;
        this.sex = sex;
        this.height = height;
        this.weight = weight;
    }
}

